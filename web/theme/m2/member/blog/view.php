                            <p style="text-align: center;"><span style="font-family:Microsoft YaHei;font-size:24px;color:#663300;background-color: rgb(255, 255, 255);">“十八铺炕”：石头的水墨丹青</span></p><p style="text-align: right;"><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">作者：朱&nbsp;&nbsp;&nbsp;姝</span></p><p style="text-align: center;"><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);"><img src="/upload/attached_blog/2015/08/normal/c17282f12cdb9f1fd614c30f92e21ce9.jpg" alt=""><br></span></p><p><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">一座向西南望见沈阳福陵、昭陵，与古意一脉相承的山；</span></p><p><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">一座向东南可远观浑河玉带，跃动生命活力的山；</span></p><p><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">一座北眺铁岭山峦叠翠，延绵丰饶且宽厚大气的山。</span></p><p><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">它就是铁南沈北第一胜景，东北独有的“鸡鸣三市”的想儿山。一座石头能发出天籁之声的“响山”，一座被无数文人墨客称颂为“蒲水观陵”的美山。</span></p><p><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">春天氤氲的晨雾还在沉睡，想儿山的一块石头伴着蒲水溪流，在悦耳的“叮咚”声中开启了大山的黎明。我是在大大小小、圆圆滚滚的石头牵引下，如醉如痴般地来到它面前的。满山遍野的春色，让我晕染了双目。野苋菜、木耳菜、蕨菜一丛丛；蒲公英碧绿中绽开朵朵黄色的小花，远远望去，似一阵云烟泛着绿波，又似金色的阳光落入草间，发出清脆的声响。荡漾的花海中黑色的玄武岩“十八铺炕”石阵，如春光中明亮的眸子时隐时现，正蓄起动人的风情，石头们刹时攫住了我的心。这十八块巨石皴以泼墨，仿佛朱耷凌空下笔，力压千钧横卧草间。或顶平，或宽大，或刚毅，或突兀，或睥睨，或妩媚，群石争秀，舒展着风采。黑色巨石镶嵌在黄花绿草中，宛若一幅空间布局奇特空灵的水墨丹青。几只小鸟蹦跳石上，时而啄食，时而远眺。小鸟看石不问石，愈淡愈真，似悟得了石头的“空无”、“本心”，十八铺炕巨石阵里藏着禅！生活在这里的人真是有福气，能这般与石头们长歌慢吟，踏青赏石，是连神仙也羡慕&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;的吧！</span></p><p><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">我想说的绝不于此！</span></p><p><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">想儿山所在的蒲河泉源头，也是满清文化繁盛之地。厚重历史衍生出优美的传说，一缕缕文化的浓香弥散在泉林白云之上。相传后金时期，占领沈阳的努尔哈赤在蒲河源演兵，就是在这样一个美丽的春天，他带十八个妃子上想儿山游玩踏青，沉醉于美景中流连忘返。此时天色已晚，仍恋恋不舍，努尔哈赤索性带着这十八个妃子住在山上的大青石头上。每人一块大石头，不多也不少。伴着清泉入梦来，是十八铺炕石头们带来的美好意境！</span></p><p><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">我抚摸着巨大的石板，石头上圆圆蜂窝状的小孔透出丝丝清凉，顺着手指尖沁入我心田。触碰着石头的体温，我发现了石头的密码。1亿4千万年前白垩纪火山爆发，玄武岩喷溢堆叠形成了有蜂窝的火山巨石阵。老百姓用奇异的石头砌院墙。每年冬去春来，冰雪融化，黑色的石头顺山而下，发出美妙声响。当地流传一句话：“一块石三两油。”石头不经历血与火的洗礼，怎会有这般厚重与淡定。</span></p><p><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">秋高气爽，我邀朋友登想儿山，于“十八铺炕”上赏完石头上的水墨丹青，然后扫净绿苔，饮酒煮茶，看枫叶正红；落雪缤纷时，听雪打石板扑簌簌，有银碗盛雪玲珑悦耳之声响⋯⋯</span></p><p><span style="font-family:Microsoft YaHei;font-size:16px;color:#663300;background-color: rgb(255, 255, 255);">&nbsp;</span></p>        </div>
