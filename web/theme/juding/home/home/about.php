                                <h2>企业简介</h2>
                                <div class="scroll jspScrollable" style="overflow: hidden; padding: 0px; width: 528px;" tabindex="0">

                                    <div class="jspContainer" style="width: 528px; height: 310px;"><div class="jspPane" style="padding: 0px; width: 514px; top: 0px;"><p>   “轩尼斯HENNISSY”品牌2004年创立伊始，至今迈入第13年的发展。轩尼斯总部根植全国著名铝材之都-佛山，是佛山市旭辉五金发展有限公司旗下品牌。目前轩尼斯门窗在广东、江苏等地拥有4大生产基地，总生产面积超过30万平方米，员工1000余人。

                                                轩尼斯门窗致力于“全球环保门窗事业”的建设，历多年的精心布局与深化规划，轩尼斯门窗于2016年成功并购澳大利亚门窗品牌SOLIDI。同年，依托SOLIDI的先进技术优势，投资建立了“轩尼斯门窗海外技术研发中心”该中心涵盖了“高科技产品技术研发”及“国际门窗市场营销”两大体系。产品遍布港澳台、欧洲、大洋洲、北美、中东及东南亚地区，受到国内及海外消费者的高度认可，品牌在国内及国外门窗行业都有着举足轻重的影响力。

                                                轩尼斯品牌主打高端豪华门窗产品，涉及室内铝制推拉门，重型铝门、生态门、卫浴门、阳光房等一系列高端产品。轩尼斯的产品材料选用优质高精度硅镁铝合金，严格按照6S质量管理体系进行管控生产；在生产技术上，轩尼斯产品不断适应市场需求发展，先后引进断桥技术、隔音技术、防腐技术、静音技术，生产出更多的优质产品以满足不同层次的消费需求。

                                                “思利及人，厚德载福”是轩尼斯品牌一直遵循的经营理念，正因为此，轩尼斯才得以不断壮大，目前在中国市场上轩尼斯门窗专卖店数量达1100余家，零售网点遍布全国各大城市。轩尼斯以卓越的品质，优质的服务和良好的业界口碑，屡获行业协会及知名媒体的赞评，并先后获得了“用户满意企业”、“广东铝门十大荣誉品牌”、“中国整体家居联盟主席单位”、“佛山市门窗业协会副会长单位”等多项殊荣。

                                                轩尼斯汇聚了业内顶尖人才以及国外知名门窗工程师，多年以来，培养出一批又一批行业人才。轩尼斯团队多次组织参加国内外家居展，其产品紧跟国际流行美学风潮，定期与国际同行展开交流学习研讨会。轩尼斯人不辱使命，勇担责任，力挺社会呼吁的“低碳环保”，缔造出花款多样的优质环保产品。轩尼斯人高瞻远瞩，放眼全球，全力开启铝制高端门窗时代新纪元。
