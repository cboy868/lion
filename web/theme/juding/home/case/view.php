        <div class="newmain" style="width:63%;float: left;overflow: hidden;">
            <p>
                <span style="font-family:SimSun;font-size:14px;color:#003399;line-height:2;">提要：</span><span style="font-size:14px;line-height:2;font-family:SimSun;color:#003399;">全国精选案例第二期，我们的摄制组来到了江苏省盐城响水县，</span><span style="font-size:14px;line-height:2;font-family:SimSun;color:#003399;">由周总带领我们走进当地李先生家。</span>
            </p>
            <p>
                <span style="font-size:12px;line-height:1.5;"> </span>
            </p>
            <p>
                <img src="/Uploads/201703/58c21defdabf8.jpg" />
            </p>
            <p>
                <br />
            </p>
            <p>
                <img src="/Uploads/201703/58c21df56a1a7.jpg" />
            </p>
            <br />
            <p>
                <br />
            </p>
            <p>
                <span style="font-size:12px;line-height:1.5;"> </span>
            </p>
            <p class="MsoNormal">
                <span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">&nbsp; &nbsp; 全国精选案例第二期，我们的摄制组来到了江苏省盐城响水县，响水轩尼斯门店开业经营已有12</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">年，可以说是与总部共同成长和发展的。响水店</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">周海军周总，是江苏轩尼斯门窗联合商会的会长，商会自2016</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">年成立以来，区域的合作加强，深度开发江苏市场的消费力，总体业绩较</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">2015</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">年增长</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">300%</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">，刷新了江苏区域的销售业绩；周 &nbsp; &nbsp; 总</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">是本地人，对他而言，每一位购买使用轩尼斯门窗的是客户，更是他的朋友。</span><o:p></o:p>
            </p>
            <p class="MsoNormal">
                <span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">这一期，就由周总带领我们摄制组的同事，走进当地李先生家。</span><o:p></o:p>
            </p>
            <p class="MsoNormal">
                <span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">&nbsp; &nbsp;&nbsp;李先生家属于高楼层复式小区住宅，采光也相较充足。进入屋里一层，家装风格主张休闲实用，选用赛拉图系列的加州白橡款，将室内阳台作半封闭式处理并到客厅区域，以帘子做隔断，空闲时候还可以做休憩室一用，晒晒太阳，谈话家常；客厅与厨房之间的隔断安装宝嘉丽吊趟推拉门与米色墙身无缝接合，与阳台窗同款色，全屋定制，自成一体，别具一格。</span><o:p></o:p>
            </p>
            <p class="MsoNormal">
                <span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">&nbsp; &nbsp; 赛拉图系列属于断桥铝门窗系列，款色可搭配黄桦木、加州白橡、酸枝、缅甸柚木、砂灰、氟碳灰、氟碳咖啡、砂咖啡、宝马金，整体标配5mm+5mm</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">的中空玻璃规格以及</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">304#</span><span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">不锈钢纱网，是一款定制款式多、性价比较高的一体化节能环保门窗。</span><o:p></o:p>
            </p>
            <p class="MsoNormal">
                <span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">因为小区管理对住宅使用门窗有规定，所以在不允许外开窗、保证安全的情况下，推拉窗凭其简便性或许会是许多家庭的首选，但也不一定能适合每一个家庭的生活方式。</span><o:p></o:p>
            </p>
            <p class="MsoNormal">
                <span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">&nbsp; &nbsp; 李先生家选定内开内倒窗的开启方式，内平开窗状态符合了小区的住宅使用要求，纱网设计在外窗不可开启，丝毫不影响室内窗户的开启，还有防虫功效；内倒状态的使用，可以避免风直吹人体，还能保证室内通风换气自然，而且不占用室内空间，避免被撞伤的可能，起到防护功能。</span><o:p></o:p>
            </p>
            <p class="MsoNormal">
                <span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">&nbsp; &nbsp; 复式的二层主要以卧室套房和书房为主，客厅的设计显得沉稳大气，加州白橡款色的赛拉图稍微点缀，便显清丽；浴室窗户也是安装同款门窗，内开内倒的开启方式可以在淋雨过程中实现通风换气不至于被冷风吹到的效果，细节的选择可以起到时刻关注和保护家人健康的作用。</span><o:p></o:p>
            </p>
            <p class="MsoNormal">
                <span style="font-family:SimSun;font-size:18px;color:#000000;line-height:2;">&nbsp; &nbsp;&nbsp;后续我们将会以视频采访的方式带给大家更多的精彩内容，通过李先生的真实讲述，对轩尼斯门窗产品的使用以及对轩尼斯门窗品牌的印象。</span><o:p></o:p>
